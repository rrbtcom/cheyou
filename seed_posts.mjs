async function main() {
  try {
    const clubs = await prisma.club.findMany({
      select: { id: true, name: true, brand: true },
      orderBy: { name: "asc" },
    });
    console.log(`共 ${clubs.length} 个车友会`);

    const clubPostInfo = new Map();
    for (const club of clubs) {
      const posts = await prisma.clubPost.findMany({
        where: { clubId: club.id },
        select: { title: true },
      });
      clubPostInfo.set(club.id, {
        brand: club.brand || club.name,
        existingCount: posts.length,
        existingTitles: new Set(posts.map((p) => p.title)),
      });
    }

    const allNewPosts = [];
    const brandStats = {};

    for (const [clubId, info] of clubPostInfo) {
      const { brand, existingCount, existingTitles } = info;
      const targetMin = 5;

      if (existingCount >= targetMin) {
        brandStats[brand] = { existing: existingCount, added: 0 };
        continue;
      }

      const needed = targetMin - existingCount;
      const available = brandPosts[brand] || [];
      const candidates = available.filter((p) => !existingTitles.has(p.title));
      const toAdd = candidates.slice(0, Math.min(needed, candidates.length));

      for (const post of toAdd) {
        const daysAgo = Math.floor(Math.random() * 30) + 1;
        allNewPosts.push({
          clubId,
          title: post.title,
          content: post.content,
          sourcePlatform: "original",
          publishedAt: new Date(Date.now() - daysAgo * 24 * 60 * 60 * 1000),
        });
      }

      brandStats[brand] = { existing: existingCount, added: toAdd.length };

      if (toAdd.length < needed) {
        console.log(`⚠️  ${brand}: 需要 ${needed} 篇但只有 ${toAdd.length} 篇候选`);
      }
    }

    console.log(`\n准备插入 ${allNewPosts.length} 篇帖子...`);

    const batchSize = 50;
    let inserted = 0;
    for (let i = 0; i < allNewPosts.length; i += batchSize) {
      const batch = allNewPosts.slice(i, i + batchSize);
      await Promise.all(
        batch.map((post) => prisma.clubPost.create({ data: post }))
      );
      inserted += batch.length;
      console.log(`  已插入 ${inserted}/${allNewPosts.length}`);
    }

    console.log("\n===== 补充结果 =====");
    let totalExisting = 0;
    let totalAdded = 0;
    const updatedBrands = [];
    for (const [brand, stats] of Object.entries(brandStats).sort()) {
      totalExisting += stats.existing;
      totalAdded += stats.added;
      if (stats.added > 0) {
        console.log(`${brand}: 原有 ${stats.existing} 篇 → 补充 ${stats.added} 篇 → 现有 ${stats.existing + stats.added} 篇`);
        updatedBrands.push(brand);
      }
    }

    console.log("\n===== 汇总 =====");
    console.log(`原有帖子总数: ${totalExisting}`);
    console.log(`本次补充: ${totalAdded}`);
    console.log(`更新后总数: ${totalExisting + totalAdded}`);
    console.log(`更新品牌数: ${updatedBrands.length}`);

    const finalCount = await prisma.clubPost.count();
    console.log(`数据库实际帖子数: ${finalCount}`);

    const insufficientBrands = [];
    for (const [clubId, info] of clubPostInfo) {
      const added = brandStats[info.brand]?.added || 0;
      if (info.existingCount + added < 5) {
        insufficientBrands.push(`${info.brand} (${info.existingCount + added}篇)`);
      }
    }
    if (insufficientBrands.length > 0) {
      console.log(`⚠️  以下品牌帖子数不足5篇: ${insufficientBrands.join(", ")}`);
    } else {
      console.log("✅ 所有品牌帖子数均 >= 5 篇");
    }

    await prisma.$disconnect();
  } catch (e) {
    console.error("执行出错:", e);
    await prisma.$disconnect();
    process.exit(1);
  }
}

main();
