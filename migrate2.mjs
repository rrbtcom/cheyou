function hashSlug(text) {
  let h = 0;
  for (let i = 0; i < text.length; i++) {
    h = ((h << 5) - h) + text.charCodeAt(i);
    h |= 0;
  }
  return text.replace(/\s+/g, "-") + "-" + Math.abs(h).toString(36).slice(0, 4);
}

